export const LORE_LIBRARY_FIRE_MAKING = `
#### Thư viện về các Phương pháp Tạo ra Lửa
- **Mục đích:** Cung cấp kiến thức nền về các cách tạo ra lửa từ thô sơ đến hiện đại và kỳ ảo, giúp AI mô tả các hành động sinh tồn hoặc các tình huống một cách chân thực và đa dạng.

**I. Phương pháp Nguyên thủy (Primitive Methods)**
*   **Ma sát (Friction):**
    *   **Khoan tay (Hand Drill):** Dùng hai lòng bàn tay xoay nhanh một que gỗ cứng trên một mảnh gỗ mềm (tấm đáy) để tạo ra bụi than nóng và bắt lửa vào bùi nhùi (tinder). Đòi hỏi sự kiên trì và kỹ năng.
    *   **Khoan cung (Bow Drill):** Hiệu quả hơn khoan tay. Dùng một cây cung và một sợi dây để xoay que gỗ với tốc độ cao hơn và ít tốn sức hơn.
*   **Va đập (Percussion):**
    *   **Đá lửa và Thép (Flint and Steel):** Dùng một mảnh thép cacbon cao (hoặc một viên đá pyrite) đập vào cạnh sắc của một viên đá lửa (flint). Tia lửa bắn ra sẽ được hứng vào bùi nhùi đã được chuẩn bị sẵn. Đây là phương pháp phổ biến thời trung cổ.

**II. Phương pháp Hiện đại (Modern Methods)**
*   **Diêm (Matches):** Que gỗ hoặc giấy có đầu tẩm hóa chất (phốt pho, lưu huỳnh) sẽ bùng cháy khi ma sát với bề mặt đặc biệt.
*   **Bật lửa (Lighter):** Thiết bị nhỏ gọn sử dụng đá lửa và một bánh xe răng cưa để tạo tia lửa, đốt cháy nhiên liệu lỏng (xăng) hoặc khí (gas). Ví dụ: Zippo, bật lửa gas.
*   **Thấu kính Hội tụ (Magnifying Glass):** Dùng thấu kính để hội tụ ánh sáng mặt trời vào một điểm trên bùi nhùi cho đến khi nó bốc cháy. Đòi hỏi trời phải có nắng.

**III. Phương pháp Kỳ ảo (Fantasy/Magical Methods)**
*   **Phép thuật Hỏa (Fire Magic):** Cách đơn giản nhất cho các pháp sư. Chỉ cần một câu thần chú hoặc cử chỉ tay để tạo ra một ngọn lửa.
*   **Dụng cụ Ma thuật:** Các vật phẩm như "Đá đánh lửa Vĩnh cửu" (Ever-burning Flint) có thể tạo ra lửa không giới hạn.
*   **Triệu hồi Tinh linh Lửa:** Gọi một tinh linh lửa nhỏ để nhóm lửa.
*   **Hơi thở của Rồng:** Lửa được tạo ra từ sinh vật huyền thoại.
`;

