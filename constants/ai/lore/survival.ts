export const LORE_LIBRARY_HUNTING_METHODS_AND_TOOLS = `
#### Thư viện về Săn bắt & Dụng cụ
- **Mục đích:** Cung cấp kiến thức nền về các phương pháp và công cụ săn bắt qua các thời kỳ, giúp AI tạo ra các tình huống sinh tồn và các nhân vật (thợ săn, du mục) một cách chân thực và có chiều sâu.

**I. Các Phương pháp Săn bắt (Hunting Methods)**
*   **Rình rập (Stalking):** Phương pháp tiếp cận con mồi một cách nhẹ nhàng và bí mật nhất có thể. Đòi hỏi sự kiên nhẫn, kỹ năng ẩn nấp và di chuyển ngược chiều gió để tránh bị phát hiện.
*   **Phục kích (Ambush):** Tìm một vị trí ẩn nấp tốt (bụi rậm, trên cây) dọc theo đường mòn hoặc gần nguồn nước mà con mồi thường lui tới và chờ đợi.
*   **Lần theo dấu vết (Tracking):** Đọc các dấu hiệu do con mồi để lại (dấu chân, phân, vết cắn trên cây) để xác định hướng di chuyển và vị trí của chúng.
*   **Săn đuổi (Persistence Hunting):** Một phương pháp săn nguyên thủy. Con người, với khả năng làm mát cơ thể bằng mồ hôi, đuổi theo một con mồi (thường là linh dương) trong một thời gian dài dưới trời nóng cho đến khi con mồi kiệt sức vì quá nhiệt.
*   **Dùng mồi nhử (Using Lures):** Bắt chước tiếng kêu của động vật hoặc sử dụng mùi hương để thu hút con mồi đến gần.

**II. Các Dụng cụ & Vũ khí Săn bắt (Hunting Tools & Weapons)**
*   **Nguyên thủy (Primitive):**
    *   **Giáo / Thương (Spear):** Vũ khí cận chiến hoặc ném tầm gần.
    *   **Cung tên (Bow and Arrow):** Vũ khí tầm xa hiệu quả nhất thời cổ đại và trung cổ.
    *   **Ná (Sling):** Dùng để ném đá với tốc độ cao.
    *   **Ống thổi tên (Blowgun):** Dùng để bắn các phi tiêu nhỏ, thường được tẩm độc, hiệu quả để săn các loài vật nhỏ.
*   **Hiện đại (Modern):**
    *   **Súng trường (Rifle):** Vũ khí tầm xa chính xác, có nhiều loại đạn khác nhau cho từng loại con mồi.
    *   **Shotgun:** Hiệu quả ở tầm gần, đặc biệt để săn các loài chim hoặc thú nhỏ đang di chuyển.
*   **Kỳ ảo (Fantasy):**
    *   **Bùa chú Ma thuật:** Dùng phép thuật để ru ngủ, trói buộc, hoặc trực tiếp tấn công con mồi.
    *   **Tên ma thuật:** Mũi tên được yểm bùa (lửa, băng, truy đuổi).
    *   **Triệu hồi Thú săn:** Gọi một linh thú (chim ưng, chó săn) để hỗ trợ việc tìm kiếm và săn bắt.
`;

export const LORE_LIBRARY_TRAPPING_METHODS = `
#### Thư viện về các Phương pháp Đặt bẫy
- **Mục đích:** Cung cấp kiến thức nền về các loại bẫy và nguyên tắc đặt bẫy, giúp AI tạo ra các tình huống sinh tồn, giải đố, hoặc các nhân vật (thợ săn, bẫy thủ) một cách thông minh và logic.

**I. Các Loại bẫy Phổ biến (Common Trap Types)**
*   **Bẫy thòng lọng (Snare Trap):**
    *   **Cơ chế:** Một vòng dây được đặt trên đường mòn. Khi con vật đi qua và đầu hoặc chân bị vướng vào, vòng dây sẽ siết chặt lại. Thường được kết nối với một cành cây cong để kéo con vật lên không trung.
    *   **Mục tiêu:** Các loài động vật nhỏ đến trung bình (thỏ, cáo, gà rừng).
*   **Bẫy hố (Pitfall Trap):**
    *   **Cơ chế:** Đào một cái hố sâu trên đường đi của con mồi và ngụy trang miệng hố bằng cành cây, lá cây. Đáy hố có thể được cắm cọc nhọn.
    *   **Mục tiêu:** Các loài động vật lớn (heo rừng, hươu, gấu, quái vật).
*   **Bẫy sập (Deadfall Trap):**
    *   **Cơ chế:** Dùng một vật nặng (khúc gỗ lớn, tảng đá) được chống lên một cách tạm bợ bằng một hệ thống cò (trigger). Khi con mồi chạm vào mồi hoặc dây bẫy, vật nặng sẽ rơi xuống và đè bẹp nó.
    *   **Mục tiêu:** Các loài động vật có kích thước đa dạng.
*   **Bẫy lồng (Cage Trap):**
    *   **Cơ chế:** Một chiếc lồng với cửa được giữ mở bằng một cơ chế cò. Khi con mồi vào trong để ăn mồi, nó sẽ kích hoạt cò và cửa lồng sập xuống, nhốt con vật lại.
    *   **Mục tiêu:** Dùng để bắt sống động vật.

**II. Các Yếu tố Cốt lõi của Bẫy**
*   **Mồi nhử (Bait):** Thức ăn hoặc vật có mùi hương hấp dẫn để dụ con mồi vào bẫy.
*   **Cơ chế Kích hoạt (Trigger Mechanism):** Bộ phận nhạy cảm của bẫy. Phổ biến nhất là dây bẫy (tripwire) và bàn đạp (pressure plate).
*   **Vị trí Đặt bẫy (Placement):** Yếu tố quan trọng nhất. Bẫy phải được đặt ở những nơi con mồi thường xuyên qua lại như đường mòn, gần nguồn nước, hoặc gần hang ổ của chúng. Phải được ngụy trang cẩn thận để hòa lẫn với môi trường.
`;
